public class NumPatternFourUsingWhileLoop
{
	public static void main(String[]ar)
	{
		int n=5,i=1;
		 i=n;
		while(i>=1)
		{
			int j=1;
			while(j<=i)
			{
				System.out.print(j+" ");
			j++;
			}
		System.out.println();
		i--;
		}
		int k=5,s=1;
		while(s<=k)
		{
			int j=1;
			while(j<=s)
			{
				System.out.print(j+" ");
			j++;
			}
		System.out.println();
		s++;
		}

	}
}